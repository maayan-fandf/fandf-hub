/**
 * Mock-data generator: drops 9 demo task rows into the Comments sheet
 * so a user can see the עטיפות feature live (umbrella + children
 * rollup, parallel-mode peers, chain-mode sequence) without setting
 * everything up by hand.
 *
 * What it creates:
 *
 *   PARALLEL umbrella ([DEMO] באנרים — Q2 קמפיין)
 *     ├─ child 1   awaiting_handling   (assignee: subject)
 *     ├─ child 2   awaiting_handling   (assignee: subject)
 *     └─ child 3   awaiting_handling   (assignee: subject)
 *
 *   CHAIN umbrella ([DEMO] השקת קמפיין FB — שרשרת)
 *     ├─ step 1 קופי     awaiting_handling   (assignee: subject)
 *     ├─ step 2 ארט      blocked              (blocked_by step 1)
 *     ├─ step 3 סטודיו   blocked              (blocked_by step 2)
 *     └─ step 4 מדיה     blocked              (blocked_by step 3)
 *
 * All 9 rows are tagged in their title prefix as `[DEMO]` so they're
 * easy to spot + delete later. They land in project="כללי", company="F&F".
 *
 * Side-effect-free: writes directly to the sheet via SA, bypassing
 * tasksCreateDirect entirely. So no Google Tasks spawn, no Drive
 * folder creation, no notifications go out. Pure visual demo.
 *
 * Subject (= "you" in the avatar/tooltip): defaults to maayan@fandf.co.il
 * — override with `node scripts/create-demo-umbrellas.mjs <email>`.
 *
 * Run:    node scripts/create-demo-umbrellas.mjs
 * Or as someone else: node scripts/create-demo-umbrellas.mjs felix@fandf.co.il
 *
 * Cleanup: open /tasks, filter by title containing "[DEMO]", delete
 * the rows manually. Or run the matching delete script if you wrote
 * one (no auto-cleanup here — these are meant to be looked at).
 */
import { google } from "googleapis";
import fs from "node:fs";

const envText = fs.existsSync(".env.local") ? fs.readFileSync(".env.local", "utf8") : "";
function envFromFile(name) {
  const line = envText.split("\n").find((l) => l.startsWith(name + "="));
  return line ? line.replace(/^[^=]+=/, "") : "";
}

const SHEET_ID = process.env.SHEET_ID_COMMENTS || envFromFile("SHEET_ID_COMMENTS");
const KEY_RAW  = process.env.TASKS_SA_KEY_JSON || envFromFile("TASKS_SA_KEY_JSON");
const SUBJECT  = (process.argv[2] || "maayan@fandf.co.il").trim().toLowerCase();
if (!SHEET_ID || !KEY_RAW) { console.log("[FAIL] env not set (need SHEET_ID_COMMENTS + TASKS_SA_KEY_JSON in .env.local)"); process.exit(1); }

const k = JSON.parse(KEY_RAW);
const auth = new google.auth.JWT({
  email: k.client_email,
  key: k.private_key,
  scopes: ["https://www.googleapis.com/auth/spreadsheets"],
  subject: SUBJECT,
});
const sheets = google.sheets({ version: "v4", auth });

const NOW = new Date().toISOString();
function genId(seed) { return `T-demo-${seed}-${Date.now().toString(36)}-${Math.random().toString(36).slice(2,6)}`; }

// Stagger rank values so tasks order naturally newest→oldest in the
// queue (rank is sorted ascending; older creations get larger negative
// numbers).
let rankCounter = -Date.parse(NOW);
function nextRank() { return ++rankCounter; }

function buildCells({
  id, title, status, isUmbrella, umbrellaId,
  blocks = [], blockedBy = [], assignees = [SUBJECT], departments = [],
  description = "DEMO row — generated by scripts/create-demo-umbrellas.mjs. Safe to delete after the toggle demo.",
}) {
  const sh0 = JSON.stringify([{ at: NOW, by: SUBJECT, from: "", to: status, note: "demo-create" }]);
  return {
    id, timestamp: NOW,
    project: "כללי", company: "F&F",
    title, body: description, brief: "",
    row_kind: "task", kind: "todo", priority: 2, status, sub_status: "",
    author_email: SUBJECT, author_name: SUBJECT.split("@")[0],
    approver_email: "", project_manager_email: "",
    requested_date: "", parent_id: "", round_number: 1,
    drive_folder_id: "", drive_folder_url: "",
    chat_space_id: "", chat_task_name: "",
    departments: JSON.stringify(departments),
    mentions: assignees.join(","),
    google_tasks: "[]",
    calendar_event_ids: "{}",
    status_history: sh0,
    edited_at: "", updated_at: NOW, campaign: "",
    rank: nextRank(), anchor: "general",
    resolved: false, revision_of: "",
    blocks: JSON.stringify(blocks),
    blocked_by: JSON.stringify(blockedBy),
    umbrella_id: umbrellaId, is_umbrella: isUmbrella ? "TRUE" : "FALSE",
  };
}

async function readHeaders() {
  const got = await sheets.spreadsheets.values.get({ spreadsheetId: SHEET_ID, range: "Comments!1:1" });
  const headers = (got.data.values?.[0] ?? []).map((h) => String(h ?? "").trim());
  return headers;
}

async function appendRows(headers, rows) {
  const values = rows.map((cells) => headers.map((h) => (h in cells ? cells[h] : "")));
  await sheets.spreadsheets.values.append({
    spreadsheetId: SHEET_ID, range: "Comments",
    valueInputOption: "RAW", insertDataOption: "INSERT_ROWS",
    requestBody: { values },
  });
}

async function run() {
  console.log(`[demo] subject=${SUBJECT}`);
  console.log(`[demo] writing 1 parallel umbrella (3 children) + 1 chain umbrella (4 steps) …`);
  const headers = await readHeaders();

  // ── PARALLEL UMBRELLA ──────────────────────────────────────────────
  const pU   = genId("paraU");
  const pC1  = genId("paraC1");
  const pC2  = genId("paraC2");
  const pC3  = genId("paraC3");
  const parallelRows = [
    buildCells({
      id: pU, isUmbrella: true, umbrellaId: "",
      title: "[DEMO] באנרים — Q2 קמפיין",
      description: "מטריה לדוגמה — 3 משימות מקבילות, אחת לכל מעצבת. כל אחת מבצעת את החלק שלה במקביל; הסטטוס של המטריה מתגבש מהילדים.",
      status: "awaiting_handling",
    }),
    buildCells({
      id: pC1, isUmbrella: false, umbrellaId: pU,
      title: "[DEMO] באנרים — Q2 קמפיין",
      description: "תת-משימה מקבילה 1/3 — DEMO row.",
      status: "awaiting_handling",
      departments: ["art"],
    }),
    buildCells({
      id: pC2, isUmbrella: false, umbrellaId: pU,
      title: "[DEMO] באנרים — Q2 קמפיין",
      description: "תת-משימה מקבילה 2/3 — DEMO row.",
      status: "awaiting_handling",
      departments: ["art"],
    }),
    buildCells({
      id: pC3, isUmbrella: false, umbrellaId: pU,
      title: "[DEMO] באנרים — Q2 קמפיין",
      description: "תת-משימה מקבילה 3/3 — DEMO row.",
      status: "awaiting_handling",
      departments: ["art"],
    }),
  ];

  // ── CHAIN UMBRELLA ──────────────────────────────────────────────────
  const cU   = genId("chainU");
  const cS1  = genId("chainS1");
  const cS2  = genId("chainS2");
  const cS3  = genId("chainS3");
  const cS4  = genId("chainS4");
  const chainRows = [
    buildCells({
      id: cU, isUmbrella: true, umbrellaId: "",
      title: "[DEMO] השקת קמפיין FB — שרשרת",
      description: "שרשרת לדוגמה — 4 שלבים סדרתיים. שלב 1 פעיל; שלבים 2–4 חסומים עד שהקודם מסתיים.",
      status: "awaiting_handling",
    }),
    buildCells({
      id: cS1, isUmbrella: false, umbrellaId: cU,
      title: "[DEMO] שלב 1 — קופי",
      description: "כתיבת ההודעה הראשית. DEMO row.",
      status: "awaiting_handling",
      blocks: [cS2], blockedBy: [],
      departments: ["copywriter"],
    }),
    buildCells({
      id: cS2, isUmbrella: false, umbrellaId: cU,
      title: "[DEMO] שלב 2 — ארט",
      description: "ויזואל ראשוני על בסיס הקופי. DEMO row.",
      status: "blocked",
      blocks: [cS3], blockedBy: [cS1],
      departments: ["art"],
    }),
    buildCells({
      id: cS3, isUmbrella: false, umbrellaId: cU,
      title: "[DEMO] שלב 3 — סטודיו",
      description: "התאמה גרפית לכל הפורמטים. DEMO row.",
      status: "blocked",
      blocks: [cS4], blockedBy: [cS2],
      departments: ["studio"],
    }),
    buildCells({
      id: cS4, isUmbrella: false, umbrellaId: cU,
      title: "[DEMO] שלב 4 — מדיה",
      description: "העלאה ל־Ads Manager. DEMO row.",
      status: "blocked",
      blocks: [], blockedBy: [cS3],
      departments: ["media"],
    }),
  ];

  await appendRows(headers, [...parallelRows, ...chainRows]);

  console.log(`✅ done — 9 rows appended.`);
  console.log(``);
  console.log(`📦 PARALLEL umbrella  /tasks/${pU}`);
  console.log(`   children: ${pC1}, ${pC2}, ${pC3}`);
  console.log(``);
  console.log(`🔗 CHAIN umbrella     /tasks/${cU}`);
  console.log(`   steps:    ${cS1} → ${cS2} → ${cS3} → ${cS4}`);
  console.log(``);
  console.log(`Try this:`);
  console.log(`  1. Open /tasks  —  you should see the 7 children but NOT the 2 umbrella rows.`);
  console.log(`  2. Click the 🪆 עטיפות chip at top  —  the 2 umbrella rows now appear inline.`);
  console.log(`  3. Click an umbrella row  —  its detail page lists all children.`);
  console.log(`  4. Mark a chain step done (status → done)  —  the next blocked step auto-unblocks.`);
  console.log(``);
  console.log(`Cleanup later: filter /tasks by title containing "[DEMO]" + delete the rows.`);
}

run().catch((e) => {
  console.log("[FATAL]", e?.message || e);
  if (e?.stack) console.log(e.stack);
  process.exit(1);
});
